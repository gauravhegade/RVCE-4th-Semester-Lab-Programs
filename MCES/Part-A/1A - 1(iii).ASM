	AREA RESET, CODE
	ENTRY
	; R1 = 3X + 4Y + 9Z -> X = 2, Y = 3, Z = 4
	; R1 WILL BE STORING RESULT
	
	MOV R1, #X		; X = 2
	MOV R2, #Y		; Y = 3
	MOV R3, #Z		; Z = 4
	
	ADD R1, R1, R1, LSL #1	
	; (3X) TO MULTIPLY BY 3 - SHIFT LEFT 1 BIT(*2) AND ADD 1
	MOV R2, R2, LSL #2
	; (4Y) TO MULTIPLY BY 4 - SHIFT LEFT 2 BITS(*4)
	ADD R3, R3, R3, LSL #3
	; (9Z) TO MULTIPLY BY 9 - SHIFT LEFT 3 BITS(*8) AND ADD 1
	
	ADD R1, R2	; 3X + 4Y
	ADD R1, R3	; (3X + 4Y) + 9Z
	
STOP B STOP
X EQU 2
Y EQU 3
Z EQU 4
	END