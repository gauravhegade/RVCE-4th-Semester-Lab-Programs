	LDR R1, [R0], #4	; LOADING LSB OF FIRST NUMBER
	LDR R2, [R0]		; LOADING MSB OF FIRST NUMBER

	LDR R0, =VALUE2
	LDR R3, [R0], #4	; LOADING LSB OF SECOND NUMBER
	LDR R4, [R0]		; LOADING MSB OF SECOND NUMBER

	ADDS R6, R2, R4 	; ADDING LSBs
	ADC R5, R1, R3		; ADDING MSBs WITH CARRY

	LDR R0, =RESULT
	STR R5, [R0], #4
	STR R6, [R0]

STOP B STOP

