	AREA RESET, CODE
	ENTRY

; FINDING GREATEST AND SMALLEST OF N-32 BIT NUMBERS

	LDR R0, =LIST
	LDR R1, [R0]		; R1 STORES SMALLEST ELEMENT
	LDR R2, [RO], #4	; R2 STORES LARGEST ELEMENT
	MOV R3, #COUNT		

LOOP
	LDR R4, [R0], #4
	CMP R4, R1
	MOVLE R1, R4
	CMP R4, R2
	MOVGE R2, R4
	
	SUBS R3, #1
	BNE LOOP

STOP B STOP
DATA1 DCD &64, &05, &96, &10, &65
COUNT EQU 4
	END