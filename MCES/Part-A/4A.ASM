	AREA RESET,CODE
	ENTRY

	; COUNT THE OCCURRENCES OF A GIVEN NUMBER IN A LIST USING LINEAR SEARCH

	LDR R0, =LIST
	MOV R1, #KEY
	MOV R2, #00		; OCCURRENCE COUNT
	MOV R3, #COUNT

LOOP
	LDR R4, [R0], #4
	CMP R4, R1
	ADDEQ R2, #1
	
	SUBS R3, #1
	BNE LOOP

	LDR R0, =0X40000000
	STR R4, [R0]

STOP B STOP

LIST DCD 0,5,1,4,100,4,0,8,7,20
KEY EQU 4
COUNT EQU 10
	END