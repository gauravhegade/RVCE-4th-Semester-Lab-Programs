; SORT GIVEN LIST OF 32BIT NUMBERS USING BUBBLE SORT
	AREA RESET, CODE
	ENTRY

	LDR R0, =LIST
	LDMIA R0, {R1-R10}
	LDR R0, =0X40000000
	STMIA R0, {R1-R10}

	MOV R2, #N
	SUBS R2, #1

OUTER
	MOV R3, R2	; INITIALIZE J, INNER LOOP
	LDR R0, =0X40000000

INNER	
	LDR R4, [R0]		; A[J]
	LDR R5, [R0, #4]	; A[J+1]
	CMP R4, R5			; IF (A[J] > A[J+1])
	STRGT R5, [R0]		; SWAP A[J](R4) AND A[J+1](R5)
	STRGT R4, [R0, #4]	

	ADD R0, #4			; GETTING NEXT ELEMENT FOR NEXT PASS
	SUBS R3, #1			; DECREMENT INNER LOOP COUNTER (J)
	BNE INNER

	SUBS R2, #1			; DECREMENT OUTER LOOP COUNTER (I)
	BNE OUTER

	
STOP B STOP
N EQU 9
LIST DCD 2, 7, 4, 5, 11, 18, 3, 15, 8, 0
	END